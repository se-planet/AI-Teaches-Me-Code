def subsets(nums):
    """返回所有子集"""
    result = []
    path = []

    def backtrack(start):
        result.append(path[:])           # 每个节点都是一个子集！

        for i in range(start, len(nums)):
            path.append(nums[i])          # 选当前数
            backtrack(i + 1)              # 递归（只能选后面的，避免重复）
            path.pop()                    # 撤销

    backtrack(0)
    return result


print(f"子集结果:")
for s in subsets([1, 2, 3]):
    print(f"  {s}")



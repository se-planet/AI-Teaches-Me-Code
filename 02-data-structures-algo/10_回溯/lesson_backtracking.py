def permute(nums):
    """返回 nums 的所有全排列"""
    result = []
    path = []
    used = [False] * len(nums)    # 标记哪些数已经选过了

    def backtrack():
        if len(path) == len(nums):          # 排满了
            result.append(path[:])           # 收集当前排列（要复制！）
            return

        for i in range(len(nums)):
            if used[i]:                      # 已经选过了，跳过
                continue
            # 做选择
            path.append(nums[i])
            used[i] = True
            # 进入下一层
            backtrack()
            # 撤销选择（回溯！）
            path.pop()
            used[i] = False

    backtrack()
    return result


print(f"全排列结果:")
for i, p in enumerate(permute([1, 2, 3]), 1):
    print(f"  {i}: {p}")


# 推演：backtrack() 的调用过程
"""
[]                    第0层
├─选1 → [1]
│   ├─选2 → [1,2]
│   │   └─选3 → [1,2,3] ✅ 收集！→ 撤销3 → 回到[1,2]
│   │   → 没别的选了 → 撤销2 → 回到[1]
│   └─选3 → [1,3]
│       └─选2 → [1,3,2] ✅ 收集！→ 一路撤销回到[]
├─选2 → [2]
│   ├─选1 → [2,1] → 选3 → [2,1,3] ✅
│   └─选3 → [2,3] → 选1 → [2,3,1] ✅
└─选3 → [3]
    ├─选1 → [3,1] → 选2 → [3,1,2] ✅
    └─选2 → [3,2] → 选1 → [3,2,1] ✅

关键：path.pop() 就是「撤销」，回到上一层，换另一个选择。
"""



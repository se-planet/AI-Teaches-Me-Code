# 一、回溯是什么？
# ============================================================
"""
例子：三个数字 [1, 2, 3] 的全排列有多少种？

树（每一步选一个还没选的数字）：

                [ ]               ← 没选
         /       |       \
       1          2         3      ← 第1步
      / \        / \       / \
     2   3      1   3     1   2    ← 第2步
     |   |      |   |     |   |
     3   2      3   1     2   1    ← 第3步

    123 132    213 231   312 321   ← 6种排列

这就是回溯：在每一层，遍历所有可能的选项 → 选一个 → 进入下一层
→ 走到底 → 回退，撤销选择 → 选下一个选项。
"""

# ============================================================
# 二、回溯模板（背下来）
# ============================================================
"""
result = []                # 存放所有解
path = []                  # 当前路径（一个解）

def backtrack(参数):
    if 满足结束条件:
        result.append(path[:])     # 收集一个解（注意要复制！）
        return

    for 选项 in 当前可选项:
        if 选项不合法: continue    # 剪枝

        做选择（把选项加入 path）
        backtrack(更新后的参数)     # 递归进入下一层
        撤销选择（把选项从 path 移除） # 回溯！

两个关键动作：
  做选择 = 往前走
  撤销选择 = 回头
"""



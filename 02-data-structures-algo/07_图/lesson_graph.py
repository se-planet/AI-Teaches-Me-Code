# ============================================================
# 一、什么是图？
# ============================================================
"""
树 vs 图：
  树：每个节点只有一个父节点（除了根）
  图：节点随便连，想连几个连几个

   A ──── B         ← A 和 B 之间有一条边
   │       │
   │       │         ← 无向图：边没有方向，A↔B
   C ──── D

术语：
  顶点（vertex）= 节点
  边（edge）= 连接
  邻居（neighbor）= 跟当前节点直接相连的节点
"""

# ============================================================
# 二、图的表示法：邻接表
# ============================================================
"""
   A ──── B
   │       │          邻接表（dict + list）：
   C ──── D          { A: [B, C], B: [A, D], C: [A, D], D: [B, C] }
                                               ↑
                                    每个节点 → 它的邻居列表
"""

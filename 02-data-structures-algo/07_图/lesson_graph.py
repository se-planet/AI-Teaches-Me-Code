# ============================================================
# 三、广度优先搜索 BFS（用队列，一层层走）
# ============================================================
"""
BFS：像水波扩散，先看最近的一圈，再看下一圈。

从 A 出发：
  第一层：A 自己
  第二层：B, C    （A 的邻居）
  第三层：D       （B 和 C 的邻居，A 已经见过了跳过）

用的就是队列——先进先出，跟你前面学的排队一样。
"""


def bfs(graph, start):
    """BFS 遍历图，返回遍历顺序"""
    visited = set()          # 记录见过的节点（避免重复）
    queue = deque([start])   # 队列：待处理节点
    order = []               # 记录顺序

    while queue:
        node = queue.popleft()     # 队头取出
        if node in visited:
            continue               # 见过了，跳过
        visited.add(node)
        order.append(node)         # 处理当前节点

        for neighbor in graph[node]:
            if neighbor not in visited:
                queue.append(neighbor)  # 邻居入队

    return order

print(f"\nBFS 从 A 开始: {bfs(graph, 'A')}")  # ['A', 'B', 'C', 'D']


# ============================================================
# 四、深度优先搜索 DFS（用栈/递归，一条路走到底）
# ============================================================
"""
DFS：一条路走到黑，没路了再回头。

从 A 出发：
  A → B → D（走到底）
  D 没新邻居 → 回到 B，没新邻居 → 回到 A → 走 C

用的是递归（调用栈），跟树的遍历一样。
"""


def dfs(graph, start, visited=None, order=None):
    """DFS 递归遍历图"""
    if visited is None:
        visited = set()
        order = []

    visited.add(start)
    order.append(start)
    print(f"  访问 {start}")        # 展示递归过程

    for neighbor in graph[start]:
        if neighbor not in visited:
            dfs(graph, neighbor, visited, order)

    return order

print(f"\nDFS 从 A 开始:")
print(f"DFS 顺序: {dfs(graph, 'A')}")  # ['A', 'B', 'D', 'C']


# ============================================================
# 五、BFS vs DFS
# ============================================================
"""
           BFS                       DFS
──────────────────────────────────────────────
数据结构   队列（deque）              递归/栈
顺序       一层层往外扩               一条路走到底
比喻       水波扩散                   钻迷宫
找最短路径  ✅ 天然适合               ❌ 不行
空间       O(宽) 可能很大             O(深) 通常较小
"""



# ============================================================
# 二、冒泡排序 — 大的往后冒
# ============================================================
"""
每轮相邻两两比较，大的往后移。每轮结束，最大的沉到最后。

[5, 3, 8, 1] 第1轮:
  5>3 换 → [3, 5, 8, 1]
  5<8 不动
  8>1 换 → [3, 5, 1, 8]  ← 8 沉底了
第2轮: [3, 5, 1, 8] → 5>1换 → [3, 1, 5, 8]
第3轮: [3, 1, 5, 8] → 3>1换 → [1, 3, 5, 8] ✅
"""


def bubble_sort(arr):
    n = len(arr)
    for i in range(n - 1):          # 需要 n-1 轮
        swapped = False
        for j in range(n - 1 - i):  # 每轮范围缩小（后面已排好）
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if not swapped:              # 本轮没交换 → 已经有序
            break
    return arr


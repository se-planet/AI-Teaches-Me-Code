print("=" * 50)
print(f"冒泡: {bubble_sort([5, 3, 8, 1])}")  # [1, 3, 5, 8]


# ============================================================
# 三、选择排序 — 挑最小的放前面
# ============================================================
"""
每轮找到剩余部分的最小值，放到前面。

[5, 3, 8, 1]
第1轮: 找最小 → 1，跟 5 换 → [1, 3, 8, 5]
第2轮: 找最小 → 3，位置对，不动  → [1, 3, 8, 5]
第3轮: 找最小 → 5，跟 8 换 → [1, 3, 5, 8] ✅
"""


def selection_sort(arr):
    n = len(arr)
    for i in range(n - 1):
        min_idx = i
        for j in range(i + 1, n):
            if arr[j] < arr[min_idx]:
                min_idx = j
        arr[i], arr[min_idx] = arr[min_idx], arr[i]  # 最小的换到前面
    return arr

print(f"选择: {selection_sort([5, 3, 8, 1])}")  # [1, 3, 5, 8]


# ============================================================
# 四、插入排序 — 像整理扑克牌
# ============================================================
"""
把元素一个一个插入到前面已排好的部分。

[5, 3, 8, 1]
拿 3: 比 5 小 → 5 右移, 3 插入前面 → [3, 5, 8, 1]
拿 8: 比 5 大 → 直接不动         → [3, 5, 8, 1]
拿 1: 比 8 小 → 8 右移
      比 5 小 → 5 右移
      比 3 小 → 3 右移, 1 插最前 → [1, 3, 5, 8] ✅
"""


def insertion_sort(arr):
    for i in range(1, len(arr)):
        key = arr[i]                # 当前要插的牌
        j = i - 1
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]     # 比 key 大的往右挪
            j -= 1
        arr[j + 1] = key            # 插入正确位置
    return arr

print(f"插入: {insertion_sort([5, 3, 8, 1])}")  # [1, 3, 5, 8]


# ============================================================
# 五、快速排序 — 分区 + 递归（面试重头戏）
# ============================================================
"""
快排核心：选一个 pivot（基准），把小于它的放左边，大于它的放右边。
然后递归排左右两边。

[5, 3, 8, 1, 9, 2]  pivot=5
分区后: [3, 1, 2]  5  [8, 9]
        左边 < 5   基准   右边 > 5

再递归排左边和右边。最终所有元素都到了正确位置。
"""


def quick_sort(arr):
    """快速排序 — 简洁版（用了额外空间，面试常用）"""
    if len(arr) <= 1:
        return arr

    pivot = arr[0]
    left = [x for x in arr[1:] if x <= pivot]
    right = [x for x in arr[1:] if x > pivot]

    return quick_sort(left) + [pivot] + quick_sort(right)


print(f"\n快排: {quick_sort([5, 3, 8, 1, 9, 2])}")


# ============================================================
# 六、归并排序 — 拆半 + 合并（分治思想）
# ============================================================
"""
归并核心：一直对半分到不能再分，然后两两有序合并。

[5, 3, 8, 1]
拆成 [5,3] 和 [8,1]
再拆 [5], [3], [8], [1]（都只有 1 个，天然有序！）
合并 [3,5] 和 [1,8] → [1,3,5,8] ← 跟前面合并有序数组一样！
"""


def merge_sort(arr):
    """归并排序"""
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left = merge_sort(arr[:mid])   # 递归排左边
    right = merge_sort(arr[mid:])  # 递归排右边

    # 合并两个有序数组（用你数组那课写的双指针合并）
    return merge(left, right)


def merge(left, right):
    """合并两个有序数组 — 你第一课就写过的！"""
    result = []
    i = j = 0
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i]); i += 1
        else:
            result.append(right[j]); j += 1
    result.extend(left[i:])
    result.extend(right[j:])
    return result


print(f"归并: {merge_sort([5, 3, 8, 1])}")


